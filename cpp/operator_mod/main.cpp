#include <iostream>

int main(int argc, char *argv[]) {
  // Try mod of negative number
  std::cout << "-5 % 4 = " << (-5 % 4) << std::endl;
  std::cout << "-2 % 4 = " << (-2 % 4) << std::endl;
  std::cout << "-1 % 4 = " << (-1 % 4) << std::endl;
  std::cout << "4 % 4 = " << 4 % 4 << std::endl;
  std::cout << "5 % 4 = " << 5 % 4 << std::endl;
  return 0;
}
